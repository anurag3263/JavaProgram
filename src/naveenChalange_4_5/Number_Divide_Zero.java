package naveenChalange_4_5;

public class Number_Divide_Zero {

	public static void main(String[] args) {
		//what we get when we divide a number by 0
		
		//System.out.println(4/0);//arithmetic exception
		
		System.out.println(4.0/0);//infinity
		
		System.out.println(4.0/0.0);//infinity
		
		System.out.println(4.87f/0);//infinity
		
		System.out.println(4.876544/0);//infinity
		
		System.out.println(0.0/0.0);//NaN----> not a number

	}

}
